package com.ernyz.dotw.Windows.Scene2dUserObects;

/**
 * This object holds data needed for scene2d buttons representing game items.
 * 
 * @author Ernyz
 */
public class ItemUserObject {
	
	/**
	 * Id of an item the button represents.
	 */
	private int itemId;
	
	public ItemUserObject() {
		
	}

	public int getItemId() {
		return itemId;
	}

	public void setItemId(int itemId) {
		this.itemId = itemId;
	}
	
}
